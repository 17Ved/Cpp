// Polymorphism in C++ --

// The word “polymorphism” means having many forms. one name and multiple forms
// In simple words, we can define polymorphism as the ability of a message to be displayed in more than one form.
// A real-life example of polymorphism is a person who at the same time can have different characteristics. 
// Like a man at the same time is a father, a husband and an employee. 
// So the same person exhibits different behavior in different situations. 
// This is called polymorphism.
// Polymorphism is considered as one of the important features of Object-Oriented Programming. 
// In C++, polymorphism is mainly divided into two types:

// Compile-time Polymorphism ---
// Compile-time polymorphism: This type of polymorphism is achieved by function overloading or operator overloading.

// Runtime Polymorphism ---
// Runtime polymorphism: This type of polymorphism is achieved by Function Overriding.
// Function overriding occurs when a derived class has a definition for one of the member functions of the base class. 
// That base function is said to be overridden. 
// achieved through virtual functions